// Define the favorite actor's name
let favActorFirstName = "Michael";
let favActorLastName = "Jackson";

// Construct the full name
let fullName = `${favActorFirstName} ${favActorLastName}`; //Using template literal

// Convert the full name to uppercase
let uppercaseFullName = fullName.toUpperCase();

// Create a message using template literals
let message = `My favorite actor is ${uppercaseFullName}. You are the best and one of my favorite movies is Karate Kid.`;


// Display the results
console.log(`First Name: ${favActorFirstName}`);
console.log(`Last Name: ${favActorLastName}`);
console.log(`Full Name: ${fullName}`);
console.log(`Uppercase Full Name: ${uppercaseFullName}`);
console.log(`Message: ${message}`);

